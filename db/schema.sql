-- ─────────────────────────────────────────────────────────────────────────
--  1. TABELLE
-- ─────────────────────────────────────────────────────────────────────────

create table if not exists public.chants (
  id              bigint generated by default as identity primary key,
  titolo          text   not null,
  testo           text   not null,
  categoria       text   not null
                  check (categoria in ('classico','incoraggiamento','curva','sfotto','derby')),
  avversario      text,
  popolarita_base int    not null default 0,   -- seed coridastadio, immutabile
  voti            int    not null default 0,   -- contatore community (denormalizzato da votes)
  stato           text   not null default 'in_attesa'
                  check (stato in ('in_attesa','pubblicato','rifiutato')),
  created_at      timestamptz not null default now()
);

create table if not exists public.media (
  id          bigint generated by default as identity primary key,
  chant_id    bigint not null references public.chants(id) on delete cascade,
  piattaforma text   not null check (piattaforma in ('youtube','tiktok','instagram')),
  url         text   not null,
  stato       text   not null default 'in_attesa'
              check (stato in ('in_attesa','approvato','rifiutato')),
  in_evidenza boolean not null default false,  -- admin marca quali mostrare tra gli approvati
  created_at  timestamptz not null default now()
);

create table if not exists public.votes (
  id          bigint generated by default as identity primary key,
  chant_id    bigint not null references public.chants(id) on delete cascade,
  voter_token text   not null,                 -- uuid casuale dal localStorage del visitatore
  created_at  timestamptz not null default now(),
  unique (chant_id, voter_token)
);

create index if not exists idx_chants_stato on public.chants(stato);
create index if not exists idx_media_chant  on public.media(chant_id);

-- ─────────────────────────────────────────────────────────────────────────
--  2. RPC VOTI — incrementa in modo sicuro, dedup per voter_token
--     SECURITY DEFINER: gira coi permessi del proprietario, così anon non
--     scrive mai direttamente la tabella votes.
-- ─────────────────────────────────────────────────────────────────────────

create or replace function public.cast_vote(p_chant_id bigint, p_token text)
returns int
language plpgsql
security definer
set search_path = public
as $$
declare
  v_count int;
begin
  -- vota solo su cori pubblicati
  if not exists (select 1 from public.chants where id = p_chant_id and stato = 'pubblicato') then
    raise exception 'coro non valido';
  end if;

  insert into public.votes (chant_id, voter_token)
  values (p_chant_id, p_token)
  on conflict (chant_id, voter_token) do nothing;

  select count(*) into v_count from public.votes where chant_id = p_chant_id;
  update public.chants set voti = v_count where id = p_chant_id;
  return v_count;
end;
$$;

grant execute on function public.cast_vote(bigint, text) to anon, authenticated;

-- ─────────────────────────────────────────────────────────────────────────
--  2b. RPC PROPOSTA CORO — inserisce coro (in attesa) + eventuali video,
--      in modo atomico. Serve perché sotto RLS l'anon non può rileggere
--      l'id di un coro non pubblicato per agganciarci i media.
-- ─────────────────────────────────────────────────────────────────────────

create or replace function public.propose_chant(
  p_titolo     text,
  p_testo      text,
  p_categoria  text,
  p_avversario text   default null,
  p_media      jsonb  default '[]'::jsonb
)
returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_id bigint;
  m    jsonb;
begin
  if coalesce(trim(p_titolo), '') = '' or coalesce(trim(p_testo), '') = '' then
    raise exception 'titolo e testo obbligatori';
  end if;
  if p_categoria not in ('classico','incoraggiamento','curva','sfotto','derby') then
    raise exception 'categoria non valida';
  end if;

  insert into public.chants (titolo, testo, categoria, avversario, stato)
  values (trim(p_titolo), p_testo, p_categoria,
          nullif(trim(coalesce(p_avversario, '')), ''), 'in_attesa')
  returning id into v_id;

  for m in select * from jsonb_array_elements(coalesce(p_media, '[]'::jsonb))
  loop
    if (m->>'piattaforma') in ('youtube','tiktok','instagram')
       and coalesce(m->>'url', '') <> '' then
      insert into public.media (chant_id, piattaforma, url, stato)
      values (v_id, m->>'piattaforma', m->>'url', 'in_attesa');
    end if;
  end loop;
end;
$$;

grant execute on function public.propose_chant(text, text, text, text, jsonb) to anon, authenticated;

-- ─────────────────────────────────────────────────────────────────────────
--  3. SICUREZZA (RLS) — l'unica parte che DEVE essere giusta
-- ─────────────────────────────────────────────────────────────────────────

alter table public.chants enable row level security;
alter table public.media  enable row level security;
alter table public.votes  enable row level security;

-- CHANTS ----------------------------------------------------------------
-- chiunque legge solo i cori pubblicati
create policy chants_read_public on public.chants
  for select to anon
  using (stato = 'pubblicato');

-- l'admin loggato legge tutto (anche le proposte in attesa)
create policy chants_read_admin on public.chants
  for select to authenticated
  using (true);

-- chiunque può proporre un coro, ma SOLO come bozza neutra (in attesa, niente voti/punteggio)
create policy chants_insert_proposal on public.chants
  for insert to anon
  with check (stato = 'in_attesa' and voti = 0 and popolarita_base = 0);

-- solo l'admin modifica/cancella (= approva, rifiuta, edita)
create policy chants_update_admin on public.chants
  for update to authenticated using (true) with check (true);
create policy chants_delete_admin on public.chants
  for delete to authenticated using (true);

-- MEDIA -----------------------------------------------------------------
-- chiunque vede solo i media approvati
create policy media_read_public on public.media
  for select to anon
  using (stato = 'approvato');

create policy media_read_admin on public.media
  for select to authenticated
  using (true);

-- chiunque può proporre un video, ma SOLO in attesa e non in evidenza
create policy media_insert_proposal on public.media
  for insert to anon
  with check (stato = 'in_attesa' and in_evidenza = false);

create policy media_update_admin on public.media
  for update to authenticated using (true) with check (true);
create policy media_delete_admin on public.media
  for delete to authenticated using (true);

-- VOTES -----------------------------------------------------------------
-- nessun accesso diretto per anon: si vota solo via RPC cast_vote().
-- l'admin può leggere per eventuali controlli.
create policy votes_read_admin on public.votes
  for select to authenticated
  using (true);

