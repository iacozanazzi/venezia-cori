-- ─────────────────────────────────────────────────────────────────────────
--  5. MERCH — prodotti con link di affiliazione (gestiti solo dall'admin)
--     Nessun seed: la lista la riempi dal pannello admin (tab "Merch").
-- ─────────────────────────────────────────────────────────────────────────

create table if not exists public.merch (
  id           bigint generated by default as identity primary key,
  titolo       text not null,
  descrizione  text,
  immagine_url text,
  prezzo       text,                       -- libero ("€24,90"): i prezzi Amazon cambiano
  link         text not null,              -- URL affiliato
  categoria    text,                       -- es. maglia, sciarpa, gadget
  attivo       boolean not null default true,
  ordine       int not null default 0,     -- ordinamento crescente in vetrina
  created_at   timestamptz not null default now()
);

create index if not exists idx_merch_attivo on public.merch(attivo);

alter table public.merch enable row level security;

-- pubblico: legge solo i prodotti attivi
create policy merch_read_public on public.merch
  for select to anon
  using (attivo = true);

-- l'admin loggato legge tutto (anche i disattivati)
create policy merch_read_admin on public.merch
  for select to authenticated
  using (true);

-- solo l'admin crea/modifica/elimina
create policy merch_insert_admin on public.merch
  for insert to authenticated with check (true);
create policy merch_update_admin on public.merch
  for update to authenticated using (true) with check (true);
create policy merch_delete_admin on public.merch
  for delete to authenticated using (true);
